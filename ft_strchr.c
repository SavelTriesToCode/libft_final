#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (*s + 1)
	{
		if (*s == (char)c)
			return ((char *)s);
		if (*s == '\0' && c == '\0')
			return ((char *)s);
		if (*s == '\0' && c != '\0')
			return (NULL);
		s++;
	}
	return (NULL);
}
